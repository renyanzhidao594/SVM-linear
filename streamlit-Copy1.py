    "# -------------------------- 1. 路径配置（替换为相对路径，兼容所有环境） --------------------------\n",
    "MODEL_PATH = \"svm_best_model.pkl\"  # 仅保留文件名，同级目录直接读取\n",
    "SCALER_PATH = \"scaler.pkl\"          # 仅保留文件名，同级目录直接读取\n",
    "FEATURE_NAMES = [\"hb\", \"tt\", \"siri\", \"afr\", \"cea_log\", \"lvi\", \"t_stage\", \"n_stage\"]\n",
    "\n",
    "# -------------------------- 2. 加载模型和标准化器 --------------------------\n",
    "try:\n",
    "    # 加载线性SVM模型并验证\n",
    "    model = joblib.load(MODEL_PATH)\n",
    "    if not (isinstance(model, SVC) and model.kernel == \"linear\"):\n",
    "        st.error(\"加载的模型不是线性SVM，请检查模型文件路径！\")\n",
    "        st.stop()\n",
    "\n",
    "    # 加载标准化器\n",
    "    scaler = joblib.load(SCALER_PATH)\n",
    "    st.success(\"模型和标准化器加载成功！\")\n",
    "    st.info(f\"特征顺序：{FEATURE_NAMES}\")\n",
    "except Exception as e:\n",
    "    st.error(f\"加载失败：{str(e)}，请检查文件路径是否正确！\")\n",
    "    st.stop()\n",
    "\n",
    "# -------------------------- 3. 网页输入界面 --------------------------\n",
    "st.title(\"Gastric Cancer Liver Metastasis Predictor\")\n",

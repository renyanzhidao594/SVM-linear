    "user_input_dict = {}\n",
    "\n",
    "# 连续型特征输入\n",
    "user_input_dict[\"hb\"] = st.number_input(\"Hemoglobin (Hb, g/L)\", min_value=30.0, max_value=200.0, value=120.0, step=0.1)\n",
    "user_input_dict[\"tt\"] = st.number_input(\"Thrombin Time (TT, sec)\", min_value=5.0, max_value=30.0, value=13.0, step=0.1)\n",
    "user_input_dict[\"siri\"] = st.number_input(\"Systemic Inflammatory Response Index (SIRI)\", min_value=0.0, max_value=10.0, value=1.0, step=0.01)\n",
    "user_input_dict[\"afr\"] = st.number_input(\"Neutrophil/Lymphocyte Ratio (AFR)\", min_value=0.1, max_value=20.0, value=2.5, step=0.01)\n",
    "\n",
    "# CEA原始值自动转换为cea_log\n",
    "cea_original = st.number_input(\"CEA Original Value (ng/mL)\", min_value=0.0, max_value=1000.0, value=1.47, step=0.1)\n",
    "cea_log = np.log10(cea_original + 1)\n",
    "user_input_dict[\"cea_log\"] = cea_log\n",
    "st.caption(f\"CEA原始值={cea_original} → cea_log≈{cea_log:.6f}\")\n",
    "\n",
    "# 分类型特征输入\n",
    "user_input_dict[\"lvi\"] = st.selectbox(\"Lymphovascular Invasion (LVI)\", options=[0, 1], format_func=lambda x: \"No (0)\" if x==0 else \"Yes (1)\")\n",
    "user_input_dict[\"t_stage\"] = st.selectbox(\"T-stage\", options=[1, 2, 3, 4], format_func=lambda x: f\"T{x}\")\n",
    "user_input_dict[\"n_stage\"] = st.selectbox(\"N-stage\", options=[0, 1, 2, 3], format_func=lambda x: f\"N{x}\")\n",
    "\n",
    "# -------------------------- 4. 构造模型输入 --------------------------\n",
    "try:\n",
    "    user_input_list = [user_input_dict[feat] for feat in FEATURE_NAMES]\n",
    "    input_data = np.array(user_input_list).reshape(1, -1)\n",
    "except KeyError as e:\n",
    "    st.error(f\"特征缺失：{str(e)}，请检查特征名称是否一致！\")\n",
    "    st.stop()\n",
    "\n",
    "# -------------------------- 5. 预测逻辑 --------------------------\n",
    "if st.button(\"Predict Liver Metastasis Risk\"):\n",
    "    # 标准化输入数据\n",
    "    input_data_scaled = scaler.transform(input_data)\n",
    "\n",
    "    # 模型预测\n",
    "    predicted_class = model.predict(input_data_scaled)[0]\n",
    "    predicted_proba = model.predict_proba(input_data_scaled)[0]\n",
    "\n",
    "    # 展示预测结果\n",
    "    st.write(\"### Prediction Result\")\n",
    "    if predicted_class == 1:\n",
    "        risk_prob = predicted_proba[1] * 100\n",
    "        st.error(f\"**Liver Metastasis Risk: High Risk**\")\n",
    "        st.write(f\"Probability of Liver Metastasis: {risk_prob:.1f}%\")\n",
    "    else:\n",
    "        risk_prob = predicted_proba[0] * 100\n",

    "        \"2. Monitor serological indicators (Hb, CEA) every 2 weeks;\\n\"\n",
    "        \"3. Consult an oncologist for adjuvant therapy (targeted/chemotherapy);\\n\"\n",
    "        \"4. Maintain a high-protein diet to improve anemia.\"\n",
    "    )\n",
    "    advice_low = (\n",
    "        \"1. Follow up (abdominal ultrasound + serology) every 3 months;\\n\"  \n",
    "        \"2. Avoid alcohol/spicy foods to reduce gastric irritation;\\n\"\n",
    "        \"3. Keep regular schedule & moderate exercise;\\n\"\n",
    "        \"4. Seek medical help for abdominal pain/jaundice/weight loss.\"\n",
    "    )\n",
    "    st.write(advice_high if predicted_class == 1 else advice_low)\n",
    "\n",
    "    # SHAP Force Plot\n",
    "    st.write(\"### Model Interpretation (SHAP Force Plot)\")\n",
    "    masker = shap.maskers.Independent(data=input_data_scaled, max_samples=100)\n",
    "    explainer = shap.LinearExplainer(model=model, masker=masker, feature_names=FEATURE_NAMES)\n",
    "    shap_values = explainer.shap_values(input_data_scaled)\n",
    "\n",
    "    # 绘制并展示力图\n",
    "    plt.figure(figsize=(12, 5))\n",
    "    shap.force_plot(\n",
    "        base_value=explainer.expected_value,\n",
    "        shap_values=shap_values,\n",
    "        features=pd.DataFrame(input_data_scaled, columns=FEATURE_NAMES),\n",
    "        matplotlib=True,\n",
    "        show=False,\n",
    "        plot_cmap=\"RdBu_r\"\n",
    "    )\n",
    "    plt.title(\"SHAP Force Plot: Feature Impact on Prediction\", fontsize=12)\n",
